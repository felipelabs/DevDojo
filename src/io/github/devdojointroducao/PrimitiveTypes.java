package io.github.devdojointroducao;

public class PrimitiveTypes {
    public static void main(String[] args) {
        // int, double, float, char, byte, short, log, boolean

        int age = 25;
        double salary = 2000;
        long number = 100000L;
        float salaryFloat = 2500.0f;
        byte ageByte = 127;
        short ageShort = 25000;
        boolean ageTrue = true;
        boolean ageFalse = false;
        char ageChar = 25;

        System.out.println("My age is " + age + " years.");
        System.out.println(ageTrue);
        System.out.println(salary);
        System.out.println(salaryFloat);

    }
}
