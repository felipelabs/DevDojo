package io.github.felipelabs.DevDojoArrays;

public class ArraysNames {
    public static void main(String[] args) {
        String[] names = new String[3];
        names[0] = "Goku";
        names[1] = "Kakaroto";
        names[2] = "Kuririm";

        for (int i = 0; i < names.length; i++){
            System.out.println(names[i]);
        }
    }
}
